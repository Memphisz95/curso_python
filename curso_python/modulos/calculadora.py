# podemos crear modulos en python
# tiene que tener la extension .py
# podemos llamar estos modulos a otros archivos py
def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

def multiplicar(a, b):
    return a * b

def dividir(a, b):
    return a / b

def potencia(a, b):
    return a ** b

def resto(a, b):
    return a % b